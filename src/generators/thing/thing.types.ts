/** Armor-specific protection configuration. */
export type HasArmor = {
  armor?: {
    /** Which equipment slot this armor occupies. */
    slot?: "Head" | "Chest" | "Hands" | "Legs";
    /** Base damage reduction as a fraction. 0.2 = 20% reduction. */
    baseDamageResistance?: number;
    /** Per-damage-type resistance modifiers. */
    resistance?: Record<string, StaticModifier[]>;
    /** Per-damage-type enhancement modifiers. */
    enhancement?: Record<string, StaticModifier[]>;
    /** Per-damage-type knockback resistance values. */
    knockbackResistances?: Record<string, number>;
    /** Entity stat modifications when armor is equipped. */
    statModifiers?: Record<string, StaticModifier[]>;
    regenerating?: {};
    /** Cosmetic items hidden when this armor is equipped to prevent clipping. */
    hide: Cosmetic[];
  };
};

/** Glider flight physics configuration. */
export type HasGlider = {
  glider?: {
    fallSpeed: {
      /** The rate at which the fall speed is incremented. */
      multiplier: number;
      /** Maximum fall speed while gliding. */
      max: number;
    };
    speed: {
      /** The rate at which the horizontal move speed is incremented. */
      multiplier: number;
      /** The horizontal movement speed of the glider. */
      speed: number;
    };
  };
};

export type StaticModifier = {
  /** string in the format
   * +10 - Additive, Max, 10
   * -10 - Additive, Max, -10
   * *1.5 - Multiplicative, Max, 1.5
   * +10min - Additive, Min, 10
   * *1.5min - Multiplicative, Min, 1.5
   */
  modifier?: string;
};

