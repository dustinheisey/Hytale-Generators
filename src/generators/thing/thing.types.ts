// =======================================================
// Common
// =======================================================
export type HasOut = {
  out?: { json: string | { root: string }; texture: string | { root: string } };
};

/** Horizontal pixel offset. Positive moves right. */
type OffsetX = number;
/** Vertical pixel offset. Positive moves down. */
type OffsetY = number;

/** Horizontal and vertical pixel offsets. */
type Vector2d = [OffsetX, OffsetY];

/** Custom display properties for icon rendering in UI. */
export interface AssetIconProperties {
  /** Scale multiplier. 1.0 is default size. */
  scale?: number;
  /** Horizontal and vertical pixel offsets [x, y]. */
  translation?: Vector2d;
  /** Rotation in degrees around each axis [x, y, z]. */
  rotation?: Vector3d;
}

/** Icon generated by the asset editor */
export type HasAssetIcon = {
  icon?: {
    path?: string;
    generated?: boolean;
    properties?: AssetIconProperties;
  };
};

/** A list of categories this item will be shown in on the creative library menu. */
export type HasCategories = { categories?: Tab[] };

/** Use to sort items into a hierarchy of where an item is in progression */
export type HasItemLevel = { level?: number };

/** The maximum amount this item can be stacked in the inventory */
export type HasMaxStack = { stackSize?: number };

/** What quality to associate with the item */
export type HasItemQuality = { quality?: AutoComplete<"common" | "epic" | "legendary" | "rare" | "uncommon"> };

/** Configuration for item behavior when dropped on the ground as an entity. */
export type HasItemEntity = {
  itemEntity?: {
    physics?: {
      /** Physics mass. Affects fall speed and inertia. */
      mass?: number;
      /** Friction coefficient when sliding on surfaces (0–1 typical). */
      friction?: number;
      /** Whether gravity affects the dropped item. */
      applyGravity?: boolean;
    };
    /**
     * Distance in blocks at which a player can pick up the dropped item.
     * @default 1.75
     */
    pickupRadius?: number;
    /** Seconds before item despawns if not picked up. */
    lifetime?: number;
    /**
     * Particle system asset displayed around dropped item.
     * @default "Item"
     */
    particleSystemId?: string;
    /** Hex color for particles (`#RRGGBB`). */
    particleColor?: string;
    /**
     * Whether to show particles around dropped items.
     * @default true
     */
    showItemParticles?: boolean;
  };
};

/** Whether to drop item on death */
export type HasDropOnDeath = { dropOnDeath?: boolean };

/** Asset set this item belongs to. Groups related items for inheritance e.g. `"Tool_Iron"`. */
export type HasSet = { set?: string };

/** Path to the 3D model asset used when held or dropped, e.g. `"Items/Rock"`. */
export type HasModel = { model?: string };

/** Scale multiplier for the item model. @default 1.0 */
export type HasScale = { scale?: number };

/** Path to the texture applied to the 3D model surface. @default "Items/Unknown.png" */
export type HasTexture<T extends string = ""> = {
  texture?: string;
  mask?: string;
  baseMask?: AutoComplete<T>;
  color?: string;
};

export interface HasItem {
  categories?: Tab | Tab[];
  level?: number;
  maxStack?: number;
  quality?: "Common" | "Uncommon" | "Rare" | "Epic" | "Legendary";
  sound?: string;
  resourceType?: string;
}

/** Animation that loops while the item is held in first or third person. */
export type HasAnimation = { animation?: string };

/**
 * When true, player hand animations drive the item display
 * instead of the item's own animations.
 * @default false
 */
export type HasUsePlayerAnimations = { usePlayerAnimations?: boolean };

/**
 * Animation set for how the player holds the item e.g. `"Sword"`, `"Pickaxe"`, `"Bow"`.
 * Controls hand position and stance when equipped.
 * @default "Default"
 */
export type HasPlayerAnimationsId = { playerAnimationsId?: string };

/** Animation played on the dropped item entity while it sits on the ground. */
export type HasDroppedItemAnimation = { droppedItemAnimation?: string };

/** Particle effects displayed around the item while held. Visible in first and third person. */
export type HasParticles = { particles?: object[] };

/** Particle effects visible only in first-person view, not to other players. */
export type HasFirstPersonParticles = { firstPersonParticles?: object[] };

/** Trail effects that follow the item model e.g. energy trails or smoke. */
export type HasTrails = { trails?: object[] };

/** RGB + brightness light emitted by the held or dropped item. */
export type HasLight = {
  light?: {
    /** Red channel (0–255). */
    red?: number;
    /** Green channel (0–255). */
    green?: number;
    /** Blue channel (0–255). */
    blue?: number;
    /** Light intensity (0–15). 15 is maximum brightness. */
    brightness?: number;
  };
};

/**
 * Map of entity stat names to appearance condition arrays.
 * Changes item appearance based on player stats e.g. at certain health thresholds.
 */
export type HasItemAppearanceConditions = { itemAppearanceConditions?: Record<string, object[]> };

/** Controls how the item behaves near walls and obstacles in first-person view. */
export type HasGeometryBehavior = {
  geometry?: {
    /**
     * When true, the item model clips through walls instead of pulling back.
     * @default false
     */
    clipsThrough?: boolean;
    /** Distance in blocks at which the arm begins to pull back near obstacles. */
    pullbackDistance?: number;
    /** Speed of the pullback animation. */
    pullbackSpeed?: number;
  };
};

/**
 * Entity stat names shown in the HUD when this item is equipped
 * e.g. `["Damage", "AttackSpeed"]`.
 */
export type HasHud = { hud?: string[] };

/**
 * Map of state names to item references or inline item definitions.
 * Used to define different states an item can be in e.g. Lit/Unlit for a torch.
 */
export type HasState = {
  state?: Record<string, string | ThingCfg> | null;
};

export type HasColor = {
  color?: string;
};

export type HasRendering = HasModel &
  HasScale &
  HasTexture &
  HasAnimation &
  HasUsePlayerAnimations &
  HasPlayerAnimationsId &
  HasDroppedItemAnimation &
  HasParticles &
  HasFirstPersonParticles &
  HasTrails &
  HasLight &
  HasItemAppearanceConditions &
  HasGeometryBehavior &
  HasHud &
  HasState;

/** Resources this item provides when harvested. Each entry has an id and optional quantity. */
export type HasResourceTypes = {
  resourceTypes?: { id: string; quantity?: number }[];
};

export type HasInteractions = {
  interactions?: {
    /**
     * Map of interaction type to interaction asset ID e.g. `{ Primary: "my_interaction" }`.
     * Defines what happens on primary/secondary use.
     */
    interactions?: Record<string, string>;
    /**
     * Key/value pairs passed as data context to interaction scripts.
     * Allows configuring interaction behavior without separate assets.
     */
    interactionVars?: Record<string, string>;

    /** Global interaction settings. */
    disabled?: boolean;
  };
};

export type HasSound = {
  sound?: {
    /** Sound effect played when the item is picked up. */
    pickup: string;
    /**
     * Sound set covering all item interactions e.g. equipping, using.
     * @default "ISS_Default"
     */
    set: string;
  };
};

