/** Tool-specific gathering configuration. */
export type HasTool = {
  tool?: {
    /** Per-material tool specifications defining power and gather type. */
    specs?: {
      /** Resource type this spec applies to e.g. `"Rocks"`, `"Wood"`. */
      gatherType: string;
      /** Effectiveness on this gather type. Higher = faster and more efficient. */
      power: number;
      /** Quality level of this tool spec. Higher quality gathers better resources. */
      quality?: number;
      /** Whether this tool is incorrect for the material. Gathers slower or not at all. */
      isIncorrect?: boolean;
      /** Sound effect when hitting this block type. */
      hitSoundLayer?: string;
    }[];
    /** Speed multiplier for tool usage. @default 1.0 */
    speed?: number;
    /** Per-material durability loss settings. */
    durabilityLossBlockTypes?: {
      /** Specific block type IDs that cause durability loss. */
      blockTypes?: string[];
      /** Block set identifiers that cause durability loss. */
      blockSets?: string[];
      /** Durability consumed per hit on specified blocks. */
      durabilityLossOnHit?: number;
    }[];
    /** Default sound when tool hits blocks. Plays in addition to block breaking sound. */
    hitCorrectSound?: string;
    /** Sound played when tool hits an incorrect material. */
    hitIncorrectSound?: string;
  };
};

