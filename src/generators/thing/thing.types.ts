/** Block-type item configuration for items that can be placed in the world. */
export type HasBlockType = {
  blockType?: {
    /** Block group for categorization e.g. `"Stone"`, `"Wood"`. `"@Tech"` prevents auto physics. */
    group?: string;
    /** Physical material. `"Empty"` = non-solid, `"Solid"` = blocks movement. */
    material?: "Empty" | "Solid";
    /** Rendering method. */
    drawType?: "Empty" | "GizmoCube" | "Cube" | "Model" | "CubeWithModel";
    /** Transparency level affecting rendering and light propagation. */
    opacity?: "Solid" | "Transparent" | "Translucent";
    /**
     * Textures for block faces. Multiple entries enable random variants via weight.
     * Use `all` for a single texture on all faces, or specify per-face textures.
     */
    textures?: {
      all?: string;
      up?: string;
      down?: string;
      north?: string;
      south?: string;
      east?: string;
      west?: string;
      /** Weight for random variant selection. Higher = selected more often. */
      weight?: number;
    }[];
    /** Path to custom 3D model. Used when drawType is `"Model"` or `"CubeWithModel"`. */
    customModel?: string;
    /** Scale multiplier for the custom model. @default 1.0 */
    customModelScale?: number;
    /** Animation asset for the custom model e.g. flowing water or lava. */
    customModelAnimation?: string;
    /** Hex color for block interaction particles (`#RRGGBB`). */
    particleColor?: string;
    /** Particle system spawned on block interaction (stepping, breaking, using). */
    blockParticleSetId?: string;
    /** Sound set for block placement, breaking, and interaction. @default "EMPTY" */
    blockSoundSetId?: string;
    /** Decal overlay showing damage progression while block is being broken. */
    blockBreakingDecalId?: string;
    /** Light emitted by the block. */
    light?: {
      red?: number;
      green?: number;
      blue?: number;
      /** Light intensity (0–15). 15 is maximum brightness. */
      brightness?: number;
    };
    flags?: {
      /** Whether the block can be interacted with. */
      isUsable?: boolean;
      /** Whether multiple blocks can occupy the same space. */
      isStackable?: boolean;
    };
    gathering?: {
      breaking?: {
        /** Resource type gathered e.g. `"Rocks"`, `"Wood"`. Determines valid tools. */
        gatherType?: string;
        /** Item ID that drops when the block is broken. */
        itemId?: string;
        /** Number of items dropped per break. */
        quantity?: number;
      };
    };
  };
};

